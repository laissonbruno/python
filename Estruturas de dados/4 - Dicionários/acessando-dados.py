dados = {"nome": "Laisson", "idade": 27, "telefone": "3333-1234"}

print(dados["nome"])  # "Laisson"
print(dados["idade"])  # 27
print(dados["telefone"])  # "3333-1234"

dados["nome"] = "Maria"
dados["idade"] = 18
dados["telefone"] = "9988-1781"

print(dados)  # {"nome": "Maria", "idade": 18, "telefone": "9988-1781"}