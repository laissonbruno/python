# fatiamento utilizando o indice

nome = "Laisson Bruno"


# vai pegar a primeira letra
print(nome[0])


# vai pegar até o nono indice menos 1. ou seja, até o indice 8
print(nome[:9])


# vai pegar a partir do indice 10 até o final
print(nome[10:])

# vai pegar do indice 10 até o indice 13.
print(nome[10:13])

# vai pegar do indice 5 até o indice 10, com um step de 2, ou seja pulando de 2 em 2.
print(nome[5:10:2])

# retorna uma copia da string
print(nome[:])

# realiza o espelhamento da string
print(nome[::-1])