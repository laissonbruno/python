# operadores de comparação são operadores para comparar dois valores.
saldo = 450
saque = 200

# maior que
print(saldo > saque)

# maior ou igual
print(saldo >= saque)

# menor que
print(saldo < saque)

# menor ou igual
print(saldo <= saque)

# igualdade a
print(saldo == saque)

# diferente de
print(saldo != saque)

