# são operadores utilizados para verificar se um objeto esta presente numa sequencia.


curso = "Curso de Python"
frutas = ["laranja", "uva", "limão"]
saques = [1500, 100]


# operador de associação ( in )
"Python" in curso

# perguntando se o objeto não esta na lista
"maçã" not in frutas

# perguntando se o objeto está na lista
200 in saques